## ---- good-code ---------------------------------------------------------

# Good Code

x0 <- 100
N <- 3  # 1.
x <- numeric(N + 1)  # 2.

for (n in 1:(N + 1)) {   # 3.
  if (n == 1) {          
    x[n] <- x0           # 4.
  } else {
    x[n] <- 0.8 * x[n - 1]  # 5.
  }
}
x


